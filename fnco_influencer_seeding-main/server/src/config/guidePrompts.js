// ── 최종 프롬프트 조립 ──
export function buildProductionGuidePrompt(cr, campaign) {
  const funnel = cr.funnel || 'TOFU';
  const hookType = cr.copy_type || '';
  const channel = (cr.campaign_placement || '').toLowerCase().replace(/[\s-]/g, '_');

  // 퍼널별 감정 전략
  const funnelGuide = FUNNEL_EMOTION_GUIDE[funnel] || FUNNEL_EMOTION_GUIDE.TOFU;

  // Hook 유형별 연출 지침
  const hookGuide = HOOK_TYPE_GUIDE[hookType]
    ? `[Hook 유형: ${hookType}]\n${HOOK_TYPE_GUIDE[hookType]}`
    : '';

  // 채널별 제작 지침
  const channelGuide = CHANNEL_GUIDE[channel] || '';

  return `${GUIDE_SYSTEM_PROMPT}

=== 캠페인 정보 ===
${buildCampaignInfo(campaign)}

=== 컨셉 정보 ===
${buildConceptInfo(cr)}

=== 퍼널 감정 전략 ===
${funnelGuide}

${hookGuide ? `=== Hook 연출 지침 ===\n${hookGuide}\n` : ''}${channelGuide ? `=== 채널 제작 지침 ===\n${channelGuide}\n` : ''}
=== 출력 JSON 형식 (반드시 이 구조로 반환) ===
${GUIDE_OUTPUT_SCHEMA}

${GUIDE_RULES}`;
}
